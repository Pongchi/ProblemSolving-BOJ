def solution(matrix_sizes):

    answer = 0
    
    while True:
        a, b = matrix_sizes.pop()
        c, d = matrix_sizes.pop()
        result = []
        if a == d:
            result = [c, b]
            answer += c * a * b
        elif b == c:
            result = [a, d]
            answer += a * c * d

        if not len(matrix_sizes):
            break

        matrix_sizes.append(result)

    return answer

